/**
 * eBay → flipagent endpoint coverage map.
 *
 * Source of truth for the /docs/coverage page. Hand-maintained TS so
 * adding a new eBay surface is one diff in one file. Re-extract from a
 * legacy .astro version with `node scripts/extract-coverage.mjs`.
 *
 * Schema:
 *   - sections: 4-column eBay → flipagent mappings, grouped by API family.
 *     Each section sets defaultSetup + defaultTier; rows override per-cell.
 *   - tradingRows: legacy XML/SOAP calls.
 *   - nativeRows: flipagent-native composite endpoints.
 *
 * `flipagent: null` means the route is intentionally not surfaced (T4).
 */

export type Setup = string;
// Tiers are mostly T1-T4, but a couple of aggregate rows (e.g. "~25
// additional Trading helpers") legitimately span tiers — keep `string`
// as the lower bound rather than a closed union.
export type Tier = "T1" | "T2" | "T3" | "T4" | "—" | (string & {});

export interface CoverageRow {
	ebay: string;
	flipagent: string | null;
	setup?: Setup;
	tier?: Tier;
}

export interface CoverageSection {
	id: string;
	title: string;
	description: string;
	defaultSetup: Setup;
	defaultTier: Tier;
	rows: CoverageRow[];
}

export interface TradingRow {
	trading: string;
	flipagent: string;
	tier: Tier;
}

export interface NativeRow {
	flipagent: string;
	composes: string;
	tier: Tier;
}

